public class StatusErrMsgVO {

	private String status;
	private String errmsg;

	public String getStatus() {
		return status;
	}

	public String getErrmsg() {
		return errmsg;
	}

	public void setErrmsg(String errmsg) {
		this.errmsg = errmsg;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
