package vo.shoppingCart;

public class ShoppingCartCategoryVO {

	private String fromsite;
	private String fromsiteimg;
	private String typ;
	private String wayremark;
	private String freight;
	public String getFromsite() {
		return fromsite;
	}
	public void setFromsite(String fromsite) {
		this.fromsite = fromsite;
	}
	public String getFromsiteimg() {
		return fromsiteimg;
	}
	public void setFromsiteimg(String fromsiteimg) {
		this.fromsiteimg = fromsiteimg;
	}
	public String getTyp() {
		return typ;
	}
	public void setTyp(String typ) {
		this.typ = typ;
	}
	public String getWayremark() {
		return wayremark;
	}
	public void setWayremark(String wayremark) {
		this.wayremark = wayremark;
	}
	public String getFreight() {
		return freight;
	}
	public void setFreight(String freight) {
		this.freight = freight;
	}
	
	
}
